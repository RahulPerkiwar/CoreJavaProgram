package BasicjavaPrep;

public class ForLoop2 {

	public static void main(String[]args)
	{
		
		
		//If Number of Iteration is fixed then we use "FOR LOOP"
		/*
		 * 
		 * Syntax  for(Initialization; condition; increment/decrement){
		 * 
		 * Statement(S);
		 * 
		 * 1) Initialization = It is initial condition which if executed once when the loop is start, 
		 * we can initialize the variable or we can use an already initialize variable
		 * 
		 * 2) It is second condition which is execute each time to test the condition of the loop. 
		 * It is continuous execution until the condition is false
		 * 
		 * 3)Increment/Decrement = It is Increment/decrement the variable value
		 * 
		 */
		
		int i;
		
		for(i=0; i<=10; i++)
		{
			
			System.out.println(i);
		}
	}
}
