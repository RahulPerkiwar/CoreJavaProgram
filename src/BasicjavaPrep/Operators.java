package BasicjavaPrep;

public class Operators 
{
	public static void main(String[]args)
	{
		
		//Airthmetic opeartos
		
		//1.Addition
		int a= 20;
		int b= 06;
		
		System.out.println(a+b);
		
		//Substraction
		
		System.out.println(a-b);
		
		//multiplication
		
		System.out.println(a*b);
		
		//divison 
		
		System.out.println(a/b);
		
		///modulus operator
		System.out.println(a%b);
		
		//increment operator (++)
		
		int c =3;
		
		c++; //3++//3+1=4
		c++; //4++//4+1 =5
		
		System.out.println(c);
		
		//decrement operator
		
		 int d = 12;
		 
		 d--; //12--//12-1=11
		
		 System.out.println(d);
		 
		 //pre increment
		 
		 int e =3;
		 
		 System.out.println(++e);//3
		 System.out.println(++e); //4
		 System.out.println(e);
		 
		 //post increment
		 System.out.println(e++);
		 System.out.println(e++); 
		 System.out.println(e);
	}

	
	
	
	
	
}
